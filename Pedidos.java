             PreparedStatement pst=cn.prepareStatement("INSERT INTO pedidos(idproducto,producto,calidad,cantidad,pulgadas) VALUES(?,?,?,?,?)");
            pst.setString(1,jTextField1.getText());
            pst.setString(2,jTextField2.getText());
